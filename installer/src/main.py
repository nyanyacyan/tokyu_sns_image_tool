# 関数定義
def main():
    url = "https://www.google.com"  # 必ず開きたいURLを指定
    driver = Chrome.get_driver(url)
    driver.get(url)
    input("Chromeブラウザが指定URLで起動していればEnterを押してください…")
    driver.quit()


# ------------------------------------------------------------------------------
if __name__ == "__main__":
    main()


